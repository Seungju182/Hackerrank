def countingSort(arr):
    # Write your code here
    min_ = min(arr)
    count = [arr.count(x) for x in range(min_, max(arr) + 1)]
    result = []
    for idx, num in enumerate(count):
        for _ in range(num):
            result.append(idx + min_)
    return result


